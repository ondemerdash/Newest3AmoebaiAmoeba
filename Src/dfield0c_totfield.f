c     ###############################################################
c     ##                                                           ##
c     ##  subroutine dfield0c  --  direct induction via Ewald sum  ##
c     ##                                                           ##
c     ###############################################################
c
c
c     "dfield0c" computes the mutual electrostatic field due to
c     permanent multipole moments via Ewald summation
c
c
c      subroutine dfield0c_3b (npole3b,pnum,field,fieldp,qgrid,qfac)
      subroutine dfield0c_totfield 
      use sizes
      use atoms
      use boxes
      use ewald
      use limits
      use math
      use mpole
      use polar, only: polarity, thole, pdamp
c      use pme
c      use chunks
c      use fft
      use totfield
      implicit none
      integer i,j,ii
      real*8 term
      real*8 ucell(3)
c      real*8 field(3,*)
c      real*8 fieldp(3,*)
c      integer npole3b,pnum(*),l1,l3
c      real*8 qgrid(2,nfft1,nfft2,nfft3)
c      real*8 qfac(nfft1,nfft2,nfft3)
c      real*8 qgrid3b(2,nfft1,nfft2,nfft3)
c      real*8 qfac3b(nfft1,nfft2,nfft3)
c      real*8 thetai1_3b(4,bsorder,n)
c      real*8 thetai2_3b(4,bsorder,n)
c      real*8 thetai3_3b(4,bsorder,n)
c      integer pmetable3b(n,nchunk)
c      integer igrid3b(3,n)
c      integer*8 planf3b
c      integer*8 planb3b
c      integer iprime3b(maxprime,3)
c      real*8 ffttable3b(maxtable,3)

c
c
c     zero out the value of the field at each site
c
      print*,"Using dfield0c_totfield aewald",aewald
      if (.not. allocated(fieldnpole)) allocate (fieldnpole(3,n))
      if (.not. allocated(fieldpnpole)) allocate (fieldpnpole(3,n))


      do i = 1, npole
         do j = 1, 3
c            field(j,i) = 0.0d0
c            fieldp(j,i) = 0.0d0
            fieldnpole(j,i) = 0.0d0
            fieldpnpole(j,i) = 0.0d0
         end do
      end do
c
c     get the reciprocal space part of the electrostatic field
c
      call udirect1 (fieldnpole)
      !call udirect1_reg (fieldnpole)

      do i = 1, npole
         do j = 1, 3
            fieldpnpole(j,i) = fieldnpole(j,i)
         end do
      end do
c
c     get the real space portion of the electrostatic field
c
c      if (use_mlist) then
c         call udirect2b (field,fieldp)
c      else
         call udirect2a (fieldnpole,fieldpnpole)
c      end if
c
c     get the self-energy portion of the electrostatic field
c

      term = (4.0d0/3.0d0) * aewald**3 / sqrtpi
      do i = 1, npole
         do j = 1, 3
            fieldnpole(j,i) = fieldnpole(j,i) + term*rpole(j+1,i)
            fieldpnpole(j,i) = fieldpnpole(j,i) + term*rpole(j+1,i)
         end do
      end do

c
c     compute the cell dipole boundary correction to field
c
      if (boundary .eq. 'VACUUM') then
         do i = 1, 3
            ucell(i) = 0.0d0
         end do
         do i = 1, npole
            ii = ipole(i)
            ucell(1) = ucell(1) + rpole(2,i) + rpole(1,i)*x(ii)
            ucell(2) = ucell(2) + rpole(3,i) + rpole(1,i)*y(ii)
            ucell(3) = ucell(3) + rpole(4,i) + rpole(1,i)*z(ii)
         end do
         term = (4.0d0/3.0d0) * pi/volbox
         do i = 1, npole
            do j = 1, 3
               fieldnpole(j,i) = fieldnpole(j,i) - term*ucell(j)
               fieldpnpole(j,i) = fieldpnpole(j,i) - term*ucell(j)
            end do
         end do
      end if
      return
      end
